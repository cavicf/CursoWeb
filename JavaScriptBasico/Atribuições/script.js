// As operações podem ser escritas da seguinte maneira para aumentar a praticidade e leitura do código:
var a = 30;

a+=2; //Isso é o mesmo que falar que a = a + 2, ou seja, o igual nessas operações diz respeito a própria varável.

console.log(a); //Imprime 32

//Isso funciona para todos os operadores
a-=2; //Mesmo que a = a-2 
console.log(a); //Imprime 30

a*=2; //Mesmo que a = a * 2
console.log(a); //Imprime 60

a/=2; //Mesmo que a = a/2
console.log(a); //Imprime 30

a%=4; //Mesmo que a = a % 4 
console.log(a); //Imprime 2