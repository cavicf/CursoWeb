// As variáveis são espaços alocados na memória para guardarmos diferentes tipos de dados. Devemos dar um nome significativo a elas, fazendo referencia ao dado que irá receber. Para declarar variáveis no javascript fazemos da seguinte forma:

var caixa = 'sapato';

// nesse caso criamos uma variavel chamada caixa e nela guardamos a palavra sapato e em seguida imprimimos o conteudo desta variavel a chamando na função console.log 

console.log(caixa)

// As variaveis como o proprio nome ja diz, podem ser modificadas a qualquer instante como vemos abaixo, onde o dado sapato foi substituido por salto 

caixa = 'salto';

console.log(caixa);